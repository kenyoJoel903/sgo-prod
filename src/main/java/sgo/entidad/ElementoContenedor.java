package sgo.entidad;

public class ElementoContenedor {
private int id;
private String valor;
/**
 * @return the id
 */
public int getId() {
 return id;
}
/**
 * @param id the id to set
 */
public void setId(int id) {
 this.id = id;
}
/**
 * @return the valor
 */
public String getValor() {
 return valor;
}
/**
 * @param valor the valor to set
 */
public void setValor(String valor) {
 this.valor = valor;
}
}
